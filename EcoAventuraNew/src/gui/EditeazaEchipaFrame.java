    public final void refreshCulori () {
        comboModel.removeAllElements();
        if (!inactiv) {
            for (String s : colors.keySet()) { 
                if (colors.get(s) == null) { // daca culoarea nu este folosita, o putem afisa
                    comboModel.addElement(s);
                }
            }

            comboModel.addElement(echipa.getCuloareEchipa());

            jComboBox1.setModel(comboModel);
        } else {
            comboModel.addElement(echipa.getCuloareEchipa());
            jComboBox1.setModel(comboModel);            
        }
        jComboBox1.setEnabled(!inactiv);
    }
    
