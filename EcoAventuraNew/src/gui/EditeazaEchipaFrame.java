    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (jTextField1.getText().equals("") || jTextField2.getText().equals("") || jTextField3.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Toate campurile trebuie completate");
            return;
        }
        
        String oldColor = echipa.getCuloareEchipa();
        
        echipa.setNumeEchipa(jTextField1.getText());
        echipa.setScoalaEchipa(jTextField2.getText());
        echipa.setProfEchipa(jTextField3.getText());
        echipa.setCuloareEchipa((String) jComboBox1.getSelectedItem());
        
        
                
        
        try {
            ControllerDB.getInstance().updateEchipa(echipa);
            colors.put(oldColor, null);
            colors.put((String) jComboBox1.getSelectedItem(), echipa);
            SerializeController.getInstance().serializareCulori(colors);
            gef.refreshEchipe();
            refreshCulori();
        } catch (Exception ex) {
            Logger.getLogger(EditeazaEchipaFrame.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Echipa nu a putut fi modificata");
        } finally {
            dispose();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

